package com.homeworkifelse;

public class ArithmeticOperators {

    public static void main(String[] args) {

        float x = 30;
        float y = 10;

        System.out.println(x+y);
        System.out.println(x-y);
        System.out.println(x*y);
        System.out.println(x/y);
        System.out.println(x++);
        System.out.println(++x);
        System.out.println(x--);
        System.out.println(--x);




    }

}
